console.log(escola.substring(0, 3)) // vai executar as 3 primeiras palavras do codigo
console.log('escola '.concat(escola).concat("!")) // vai misturar uma strig com a execução do codigo
console.log(escola.replace(3, 'e')) // vai substituir todos os números 3 pela letra "e"
